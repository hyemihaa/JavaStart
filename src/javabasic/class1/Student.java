package javabasic.class1;

/*
* 학생 클래스 -> 학생이라는 타입
* 사용자가 직접 정의하는 사용자 정의 타입을 만드려면 설계도가 필요 -> 클래스
* 클래스를 사용해서 실제 메모리에 만들어진 실체 -> 객체 또는 인스턴스
* 클래스(설계도, 틀) = 붕어빵 틀, 객체(인스턴스) = 실제 먹을수 있는 붕어빵
* 클래스(설계도, 틀) = 자동차 설계도(실제 존재X, 개념) 객체(인스턴스) = 흰색 자동차
 */ 
public class Student {
    // 클래스에 정의한(소속된) 변수(속성) -> 멤버변수 ,필드라 함
    String name;
    int age;
    int grade;
}
